#include <stdio.h>

int main(){

	int n, room = 1, cnt = 1;
	scanf("%d", &n);

	while (1)
	{
		if ( n - room <= 0)
		{
			break;
		}

		room += cnt * 6;
		cnt++;

	}
	printf("%d", cnt);
	return 0;


}

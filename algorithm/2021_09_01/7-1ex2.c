#include <stdio.h>
#include <stdlib.h>

int main(){
	int *pi, i;

	pi = (int *)malloc(5 * sizeof(int));
	if(pi == NULL){
		printf("fail");
		exit(1);
	}
	pi[0] = 100;
	pi[1] = 200;
	pi[2] = 3100;
	pi[3] = 4100;
	pi[4] = 5100;

	for ( i = 0; i < 5; i++)
	{
		printf("%d\n", pi[i]);
	}
	free(pi);


	return 0;
}

#include <stdio.h>

int main(){
	int h, m;

	scanf("%d %d", &h, &m);
	m -= 45;

	if(m < 0 && h != 0 ){
		printf("%d %d ", h-1, m + 60);
	}else if(h == 0 && m < 0){
		printf("%d %d ", h = 23, m + 60);
	}else if(h == 0 && m >= 0){
		printf("%d %d ", h , m);
	}
	else if(m < 0){
		printf("%d %d ", h, m + 60);
	}else{
		printf("%d %d", h, m);
	}
	return 0;
}
